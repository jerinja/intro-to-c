#include<stdio.h>
    
int main()
{
    // for integer values //
    int j = 45;
    printf("%d",j);
  
    // for decimal values //
    float f = 45.4;
    printf("%f",f);
  
    // for large decimal values //
    double d = 1234.568790;
    printf("%f",d);

    // for character values //
    char ch = 'y';
    printf("%c",ch);
  
    return 0;
}
